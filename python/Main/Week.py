x = int(input("Enter a number 1-7 "))
if x == 1:
    print("Monday",x)
elif x == 2:
    print("Tuesday",x)
elif x == 3:
    print("Wednesday",x)
elif x == 4:
    print("Thursday",x)
elif x == 5:
    print("Friday",x)
elif x == 6:
    print("Saturday",x)
elif x == 7:
    print("Sunday",x)
else:
    print("Invalid")